# Zainicjalizowanie listy "list"
list = [1, 2.2, "three", 4.9, 5, 6.7, "seven", "eight", 9.3, 10]

# Wyświetlenie funkcjonalności listy
puts "Zawartość listy:"
puts list

puts "Zawartość listy w jednej linii:"
puts list.inspect

puts "Element listy pod indeksem 2:"
puts list[2]

puts "Element listy pod indeksem -1:"
puts list[-1]

puts "Pierwszy element listy:"
puts list.first

puts "Ostatni element listy:"
puts list.last

puts "Długość listy:"
puts list.length
